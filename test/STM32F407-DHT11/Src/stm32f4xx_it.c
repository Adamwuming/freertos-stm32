/**
* @brief This function handles USART1 global interrupt.
*/
void USART1_IRQHandler(void)
{
  /* USER CODE BEGIN USART1_IRQn 0 */
  if(((__HAL_UART_GET_FLAG(&huart1, UART_FLAG_RXNE)) != RESET) && ((__HAL_UART_GET_IT_SOURCE(&huart1, UART_IT_RXNE)) != RESET))
  { 
    prvvUARTRxISR();
    HAL_NVIC_ClearPendingIRQ(USART1_IRQn);
  }

  if(((__HAL_UART_GET_IT_SOURCE(&huart1, UART_IT_TXE)) != RESET) && ((__HAL_UART_GET_FLAG(&huart1, UART_FLAG_TXE)) != RESET))
  {
    prvvUARTTxReadyISR();
    HAL_NVIC_ClearPendingIRQ(USART1_IRQn);
  }
	
  /* USER CODE END USART1_IRQn 0 */
  HAL_UART_IRQHandler(&huart1);
  /* USER CODE BEGIN USART1_IRQn 1 */

  /* USER CODE END USART1_IRQn 1 */
}

/**
* @brief This function handles USART3 global interrupt.
*/
void USART3_IRQHandler(void)
{
  /* USER CODE BEGIN USART3_IRQn 0 */
  uint32_t tmp1, tmp2;	
  tmp1 = __HAL_UART_GET_FLAG(&huart3, UART_FLAG_RXNE);
  tmp2 = __HAL_UART_GET_IT_SOURCE(&huart3, UART_IT_RXNE);
  if((tmp1 != RESET) && (tmp2 != RESET))
  {
		LED_Toggle(2);
    FillConsoleBuf((huart3.Instance->DR & (uint8_t)0x00FFU));
  }
  /* USER CODE END USART3_IRQn 0 */
  HAL_UART_IRQHandler(&huart3);
  /* USER CODE BEGIN USART3_IRQn 1 */

  /* USER CODE END USART3_IRQn 1 */
}

